package com.prueba.models;

/**
 *
 * @author Edinson
 */
public class Usuario {
     private String  nombre, cedula, usuario, clave, tipo, fecha;

    public Usuario() {
    }

    public Usuario(String nombre, String cedula, String usuario, String clave, String tipo, String fecha) {
        this.nombre = nombre;
        this.cedula = cedula;
        this.usuario = usuario;
        this.clave = clave;
        this.tipo = tipo;
        this.fecha = fecha;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCedula() {
        return cedula;
    }

    public void setCedula(String cedula) {
        this.cedula = cedula;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getClave() {
        return clave;
    }

    public void setClave(String clave) {
        this.clave = clave;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }


}
